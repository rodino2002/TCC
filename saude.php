                    <label class="text-default text-justify"> 
                        <strong><h2>1. Água </h2></strong>
                  A água é um dos principais remédios que a natureza oferece e 80% do nosso cérebro. Além disso, esse líquido regula uma série de funções do organismo. A água contribui para o equilíbrio término, para o bom funcionamento do intestino, evita cálculos renais, entre outros problemas.</br><a href="#" name="ar"></a>

            A água participa ainda de todos os processos químicos que acontecem no organismo. Nosso corpo funciona devido aos estímulos elétricos produzidos (e recebidos) pelo sistema nervoso. A água possui minerais que a tornam uma condutora de eletricidade. Portanto, ela permite a comunicação entre as células e o bom funcionamento dos nossos sistemas.</br>
            Portanto, tomar água em grande quantidade é fundamental para manter ou restabelecer a saúde.</br></br>

            <hr class="bg-primary">
            <strong><h2>2. Ar Puro</h2></strong>
            O ar é o segundo dos remédios naturais. O ideal é vivermos em ambientes com baixos índices de poluição, onde o ar ainda é puro, livre de partículas e componentes químicos que prejudicam o organismo. Porém, sabemos que nem sempre isso é possível.</br>

            Por isso, nossa recomendação é de que, quando houver oportunidade, você fique próximo da natureza. Tente caminhar em um bosque ou parque pelo menos nos finais de semana. Porém, não é só a qualidade do ar que influencia nossa saúde. Também é fundamental respirar profundamente.</br><a href="#" name="alimentacao"></a>

            A maneira correta de respirar é inspirar pelo nariz, absorvendo o máximo de ar possível. O tórax deve se dilatar nesse momento, fazendo com que o oxigênio encha completamente os pulmões. Ao expirar, também é importante expelir todo o ar que estava dentro dos pulmões para eliminar o dióxido de carbono completamente.</br></br>
            <hr class="bg-primary">
            <strong><h2>3. Alimentação Saudável</h2></strong>
            A alimentação é um dos principais remédios naturais. Ela precisa conter todos os macronutrientes (carboidratos, proteínas e gorduras) e micronutrientes (vitaminas e minerais) que o corpo utiliza para obter energia, produzir novas células e regular suas funções.</br>
            Manter uma alimentação saudável é mais simples do que as pessoas imaginam. Porém, como nosso paladar está habituado e a sentir prazer com opções que não fazem bem ao corpo, a mudança de hábitos pode ser desafiadora.</br></br><a href="#" name="exercicio"></a>

            alimentação saudável</br>
            A alimentação pode ser fonte de cura ou também provocar uma série de doenças. Por isso, vale a pena mudar o cardápio, nem que para isso seja necessário passar por um processo gradual de aprender a ingerir alimentos saudáveis e eliminar os que não fazem bem.</br></br>
            <hr class="bg-primary">
            <strong><h2>4. Exercício</h2></strong>
            O exercício é outro fator fundamental para uma boa saúde. Além da sensação de bem-estar obtida através da produção de endorfinas, ele ajuda a dormir melhor, a manter o peso estável, contribui para evitar e a superar a depressão e está relacionado à prevenção do câncer, entre outros benefícios.</br>
            A atividade física ainda preserva a massa muscular, contribuindo para manter a força física, proteger as articulações e favorecer a mobilidade. Também não podemos nos esquecer de seu papel no fortalecimento do coração, estímulo ao bom funcionamento do intestino, aumento da imunidade e preservação das funções cognitivas, devido à produção de neurotransmissores que beneficiam o tono psíquico e o humor.</br><a href="#" name="repouso"></a>
            A quantidade de exercício recomendada pela Organização Mundial da Saúde é de 150 minutos semanais de atividades moderadas ou 75 minutos de atividade intensa. Portanto, bastam 5 sessões de 30 minutos por semana para melhorar muito sua saúde e qualidade de vida.</br></br>
            <hr class="bg-primary">
            <strong><h2>5. Repouso</h2></strong>
            O repouso é tão importante quanto o exercício. O corpo e o cérebro precisam desse período de descanso para se recuperarem do esforço do dia, organizar as informações recebidas, produzir novas células e hormônios importantes para regular o funcionamento do organismo.</br>
            A falta do sono reparador está associada a uma série de problemas de saúde física e mental. A insônia aumenta as chances de desenvolver doenças como diabetes, problemas cardiovasculares, inflamações, depressão e outros transtornos mentais.</br><a href="#" name="temperanca"></a>
            Portanto, se você não consegue dormir bem, é importante se esforçar para começar a ter noites reparadoras. A higiene do sono é um conjunto de ações que podem ajudá-lo a condicionar seu corpo e mente para esse momento de descanso que é tão importante para nossa saúde e bem-estar.</br></br>
            <hr class="bg-primary">
           <strong><h2>6. Temperança</h2></strong>
            A temperança é uma palavra que muitas pessoas preferem substituir por equilíbrio. Trata-se da capacidade de usar com moderação aquilo que nos faz bem e praticar a abstinência em relação ao que provoca doenças. Alguns exemplos de hábitos que precisam ser eliminados são o tabagismo, uso de substâncias químicas, consumo de álcool e alimentos adoçados, entre outros.</br><a href="#" name="luz"></a>
            Embora alguns desses hábitos sejam aceitos socialmente e culturalmente, é importante saber que eles são fatores causadores de doenças. Um exemplo é o consumo de álcool, tão presente no convívio social. Na verdade, não existe um nível seguro para ingestão de bebidas alcoólicas. Mesmo em doses moderadas, elas aumentam os riscos de câncer, por exemplo.</br></br>
           <hr class="bg-primary">
            <a href="#" name="Deus"></a>
            <strong><h2>7. Luz Solar</h2></strong>
            Durante anos, a comunidade científica alertou a população sobre os perigos de tomar sol. No entanto, hoje as pesquisas mostram que, embora a hiperexposição realmente não seja recomendada, o contato diário com a luz solar é muito importante para a manutenção da saúde.</br>
            A luz solar promove uma série de reações químicas no organismo. Quando a pele é exposta ao sol, seus raios desencadeiam mecanismos de controle da pressão arterial, por exemplo. Também não podemos nos esquecer da vitamina D, que na verdade é um hormônio que traz muitos benefícios e é associado à prevenção de uma série de doenças.</br>
            Portanto, a exposição controlada à luz solar, sem uso de protetores químicos, deve fazer parte da nossa rotina.</br></br>
            <hr class="bg-primary">

           <strong><h2>8. Confiança em Deus</h2></strong>
            As diferentes academias que pesquisam e divulgam a Medicina do Estilo de Vida apresentam esse remédio natural de forma diferente. Algumas se referem a ele como gratidão, outras como fé e ainda há os que preferem denominá-lo espiritualidade.</br>
            O fato é que, independentemente de sua religião, vários estudos demonstram que a fé ou espiritualidade é capaz de gerar significado e valor para a vida humana, além de influenciar positivamente na saúde e processos de cura.</br>
            Mário Borba, diretor científico de um projeto da Sociedade Brasileira de Cardiologia que estuda justamente a relação entre espiritualidade e a Medicina, afirma que “já não temos dúvidas de que a fé contribui para a saúde. Queremos entender melhor agora até onde vão seus efeitos e de que forma ela os propicia”.</br>

